/**
 * @file    tm1637.h
 * @brief   Драйвер дисплея TM1637 для 4-розрядного 7-сегментного індикатора
 */

#ifndef TM1637_H
#define TM1637_H

#include "board_config.h" 
#include "clock.h"    

/* ========================================================================== */
/* АПАРАТНІ МАКРОСИ (Налаштування під плату термостата)                       */
/* ========================================================================== */
/* DIO підключено до PC6, CLK підключено до PC7                               */
/* Режим пінів: Open-Drain (Відкритий стік). 1 - відпустити, 0 - до землі.    */

#define DIO_HIGH()  (TM1637_PORT_ODR |= TM1637_DIO_PIN)
#define DIO_LOW()   (TM1637_PORT_ODR &= ~TM1637_DIO_PIN)

#define CLK_HIGH()  (TM1637_PORT_ODR |= TM1637_CLK_PIN)
#define CLK_LOW()   (TM1637_PORT_ODR &= ~TM1637_CLK_PIN)

/* Макрос затримки */
#define DELAY_10US() delay_us(10) 

/* ========================================================================== */
/* ПУБЛІЧНІ ФУНКЦІЇ (API)                                                     */
/* ========================================================================== */

/**
 * @brief Виводить 4 сирі байти (7-сегментні коди) на дисплей
 * @param d0 Найлівіший символ (GRID1)
 * @param d1 Другий символ (GRID2)
 * @param d2 Третій символ (GRID3)
 * @param d3 Найправіший символ (GRID4)
 */
void TM1637_ShowRaw(uint8_t d0, uint8_t d1, uint8_t d2, uint8_t d3);

#endif /* TM1637_H */