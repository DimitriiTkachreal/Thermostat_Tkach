/**
 * @file    buttons.h
 * @brief   Драйвер кнопок з програмним антибрязкотом (FSM).
 * @details Не використовує зовнішніх переривань (EXTI) на пінах. 
 * Вимагає виклику Buttons_Process() у головному циклі програми.
 */

#ifndef BUTTONS_H
#define BUTTONS_H

#include "board_config.h"

/* ========================================================================== */
/* ТИПИ ДАНИХ (ПУБЛІЧНІ)                                                      */
/* ========================================================================== */

/* Ідентифікатори кнопок */
typedef enum
{
    BTN_UP = 0,
    BTN_DOWN,
    BTN_SET,
    BTN_COUNT /* Кількість кнопок = 3 */
} button_id_t;

/* Події, які може віддати автомат станів */
typedef enum
{
    BTN_EVENT_NONE = 0, /* Нічого не сталось */
    BTN_EVENT_CLICK,    /* Коротке натискання (після 20 мс) */
    BTN_EVENT_HOLD      /* Довге утримання (> 1 сек) */
} button_event_t;

/* ========================================================================== */
/* ПУБЛІЧНІ ФУНКЦІЇ (API)                                                     */
/* ========================================================================== */

/**
 * @brief Ініціалізує контекст автомата станів.
 */
void Buttons_Init(void);

/**
 * @brief Головний процес обробки. Викликати в нескінченному циклі.
 */
void Buttons_Process(void);

/**
 * @brief Повертає подію для обраної кнопки та обнуляє її.
 * @param id Кнопка (BTN_UP, BTN_DOWN, BTN_SET)
 * @return Подія (NONE, CLICK, HOLD)
 */
button_event_t Buttons_GetEvent(button_id_t id);

#endif /* BUTTONS_H */