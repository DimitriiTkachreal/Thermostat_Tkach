/**
 * @file    ntc.h
 * @brief   Драйвер термістора NTC 10K 3950.
 * @details Відповідає за читання сирих даних АЦП, фільтрацію шуму (усереднення) 
 * та конвертацію в градуси Цельсія за допомогою лінійної інтерполяції.
 */

#ifndef NTC_H
#define NTC_H

#include "adc.h"

/* ========================================================================== */
/* МАКРОСИ ПОМИЛОК                                                            */
/* ========================================================================== */
/* Якщо датчик відірваний або закорочений, функція поверне це число */
#define NTC_ERROR_VALUE (-999)

/* ========================================================================== */
/* ПУБЛІЧНІ ФУНКЦІЇ (API)                                                     */
/* ========================================================================== */

/**
 * @brief   Зчитує температуру з термістора.
 * @details Робить 64 виміри, усереднює їх (відкидаючи шум) і за допомогою 
 * таблиці шукає відповідну температуру з точністю до десятих.
 * @param   None
 * @return  int16_t Температура, помножена на 10 (наприклад, 253 = 25.3 °C).
 * У разі апаратної помилки повертає NTC_ERROR_VALUE.
 */
int16_t NTC_GetTemperature(void);

#endif /* NTC_H */