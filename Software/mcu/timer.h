/**
 * @file    timer.h
 * @brief   Драйвер системного часу (SysTick) на базі TIM4.
 * @details Налаштовує апаратний таймер для генерації переривань кожну 
 * 1 мілісекунду. Надає функцію для отримання глобального часу.
 */

#ifndef TIMER_H
#define TIMER_H

#include "board_config.h" 

/* ========================================================================== */
/* ПУБЛІЧНІ ФУНКЦІЇ (API)                                                     */
/* ========================================================================== */

/**
 * @brief   Ініціалізує Таймер 4.
 * @details Встановлює дільник та межу рахунку для кроку в 1 мс.
 * @param   None
 * @return  None
 */
void Timer4_Init(void);

/**
 * @brief   Повертає поточний системний час.
 * @details Використовує атомарне читання для захисту 32-бітної змінної 
 * від пошкодження перериванням на 8-бітній архітектурі.
 * @param   None
 * @return  uint32_t Кількість мілісекунд з моменту подачі живлення.
 */
uint32_t Timer_GetMillis(void);

#endif /* TIMER_H */