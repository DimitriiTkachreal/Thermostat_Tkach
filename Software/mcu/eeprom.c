#include "eeprom.h"

/* ========================================================================== */
/* ПРИВАТНІ ФУНКЦІЇ (Керування сейфом MASS)                                   */
/* ========================================================================== */

static void EEPROM_Unlock(void) 
{
    /* Перевіряємо, чи сейф раптом вже не відкритий */
    if ((FLASH_IAPSR & FLASH_IAPSR_DUL) == 0) 
    {
        /* Вводимо два ключі суворо один за одним (згідно з RM0016) */
        FLASH_DUKR = 0xAE;
        FLASH_DUKR = 0x56;
    }
    
    /* Чекаємо, поки залізо переварить ключі і підніме прапорець "Відкрито" */
    while ((FLASH_IAPSR & FLASH_IAPSR_DUL) == 0) 
    {
        /* Блокуємо ядро. Далі йти не можна, поки сейф закритий */
    }
}

static void EEPROM_Lock(void) 
{
    /* Примусово скидаємо прапорець DUL в нуль, закриваючи сейф */
    FLASH_IAPSR &= (uint8_t)(~FLASH_IAPSR_DUL);
}

/* ========================================================================== */
/* ПУБЛІЧНІ ФУНКЦІЇ (API)                                                     */
/* ========================================================================== */

uint8_t EEPROM_ReadByte(uint16_t address) 
{
    /* Створюємо вказівник, який дивиться на потрібну адресу в пам'яті. 
     * volatile забороняє компілятору оптимізувати це читання. */
    volatile uint8_t *ptr = (volatile uint8_t *)address;
    
    /* Читати можна завжди, без ключів. Просто беремо значення. */
    return *ptr;
}

void EEPROM_WriteByte(uint16_t address, uint8_t data) 
{
    volatile uint8_t *ptr = (volatile uint8_t *)address;
    
    /* 1. Відкриваємо захист */
    EEPROM_Unlock();
    
    /* 2. Фізичний запис електронів у плаваючий затвор */
    *ptr = data;
    
    /* 3. Миттєво закриваємо захист */
    EEPROM_Lock();
}