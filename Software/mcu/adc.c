/**
 * @file    adc.c
 * @brief   Реалізація низькорівневої логіки ADC1 з використанням бітових масок.
 */

#include "adc.h"
#include "clock.h"


void ADC_Init(void) 
{
    CLK_PCKENR2 |= CLK_PCKENR2_ENABLE_BIT;
    /* ====================================================================== *
     * 1. НАЛАШТУВАННЯ КАНАЛУ (МУЛЬТИПЛЕКСОР)
     * Регістр: ADC1_CSR | Біти: CH[3:0] (позиції 3, 2, 1, 0)
     * Канал: AIN2 (двійкове 0010). Треба встановити в '1' тільки біт 1.
     * ====================================================================== */
    /* Очищаємо всі 4 молодші біти, щоб зняти попередні налаштування */
    ADC1_CSR &= ~ADC1_CSR_CH_MASK;
    /* Встановлюємо 1-й біт для вибору каналу 2 */
    ADC1_CSR |= NTC_ADC_CHANNEL;


    /* ====================================================================== *
     * 2. ВИРІВНЮВАННЯ ДАНИХ (ALIGN)
     * Регістр: ADC1_CR2 | Біт: ALIGN (позиція 3)
     * Значення: 1 (Праве вирівнювання для отримання повних 10 біт)
     * ====================================================================== */
    ADC1_CR2 |= ADC1_CR2_ALIGN_RIGHT;


    /* ====================================================================== *
     * 3. ДІЛЬНИК ЧАСТОТИ (PRESCALER)
     * Регістр: ADC1_CR1 | Біти: SPSEL[2:0] (позиції 6, 5, 4)
     * Частота ядра: 16 МГц. Потрібно: 4 МГц. Ділимо на 4.
     * Код ділення на 4 згідно RM0016: 001 (встановити в '1' тільки біт 5.
     * ====================================================================== */
    /* Очищаємо всі три біти дільника */
    ADC1_CR1 &= ~ADC1_CR1_SPSEL_MASK;
    /* Встановлюємо 5-й біт */
    ADC1_CR1 |= ADC1_CR1_SPSEL_FMASTER_DIV4;


    /* ====================================================================== *
     * 4. ПРОБУДЖЕННЯ АЦП (POWER ON)
     * Регістр: ADC1_CR1 | Біт: ADON (позиція 0)
     * Запис '1' вмикає аналогову частину. Вимірювання ще не починається.
     * ====================================================================== */
    ADC1_CR1 |= ADC1_CR1_ADON;
    delay_us(20);
}

uint16_t ADC_Read(void) 
{
    uint8_t low_byte;
    uint8_t high_byte;
    uint16_t result;

    /* 1. СТАРТ ВИМІРЮВАННЯ
     * Повторний запис '1' у біт ADON працює як апаратний тригер для початку вимірювання. 
     */
    ADC1_CR1 |= ADC1_CR1_ADON;

    /* 2. ОЧІКУВАННЯ (POLLING)
     * Регістр: ADC1_CSR | Біт: EOC (позиція 7)
     * Блокуємо ядро, поки АЦП не встановить цей біт в '1'.
     */
    while ((ADC1_CSR & ADC1_CSR_EOC) == 0) 
    {
        /* Чекаємо ~3.5 мікросекунди */
    }

    /* 3. ОЧИЩЕННЯ ПРАПОРЦЯ (HANDSHAKE)
     * Скидаємо біт EOC в '0', щоб АЦП міг сигналізувати про наступний вимір.
     */
    ADC1_CSR &= ~ADC1_CSR_EOC;


    /* 4. ЗЧИТУВАННЯ ТІНЬОВИХ РЕГІСТРІВ
     * Для Правого вирівнювання (ALIGN=1) ПЕРШИМ суворо читається DRL.
     * Це апаратно блокує зміну регістра DRH до його прочитання.
     */
    low_byte = ADC1_DRL;
    high_byte = ADC1_DRH;

    /* 5. ФОРМУВАННЯ РЕЗУЛЬТАТУ
     * Розширюємо 8-бітний high_byte до 16 біт, зсуваємо на 8 позицій вліво
     * і накладаємо low_byte за допомогою побітового АБО.
     */
    result = ((uint16_t)high_byte << 8) | low_byte;

    return result;
}