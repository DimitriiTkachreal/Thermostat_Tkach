/**
 * @file    gpio.c
 * @brief   Реалізація конфігурації пінів.
 */

#include "gpio.h"


void GPIO_Init(void) 
{
    /* ---------------------------------------------------------------------- */
    /* 1. КНОПКИ: PD2 (UP), PD3 (DOWN), PC5 (SET)                             */
    /* Налаштування: ВХІД, ПЛАВАЮЧИЙ (Floating), без переривань.              */
    /* Причина: На платі є зовнішні підтягуючі резистори R10, R11, R12.       */
    /* ---------------------------------------------------------------------- */
    /* Порт D: піни 2 і 3 */
    GPIOD_DDR &= ~(BTN_UP_PIN | BTN_DOWN_PIN); /* 0: Вхід */
    GPIOD_CR1 &= ~(BTN_UP_PIN | BTN_DOWN_PIN); /* 0: Floating */
    GPIOD_CR2 &= ~(BTN_UP_PIN | BTN_DOWN_PIN); /* 0: Переривання вимкнені */
    /* Порт C: пін 5 */
    GPIOC_DDR &= ~BTN_SET_PIN;
    GPIOC_CR1 &= ~BTN_SET_PIN;
    GPIOC_CR2 &= ~BTN_SET_PIN;

    /* ---------------------------------------------------------------------- */
    /* 2. РЕЛЕ: PC3                                                           */
    /* Налаштування: ВИХІД, PUSH-PULL, низька швидкість (2 МГц).              */
    /* Причина: Потрібно жорстко керувати затвором транзистора Q4.            */
    /* ---------------------------------------------------------------------- */
    RELAY_PORT_DDR |= RELAY_PIN;  /* 1: Вихід */
    RELAY_PORT_CR1 |= RELAY_PIN;  /* 1: Push-Pull */
    RELAY_PORT_CR2 &= ~RELAY_PIN; /* 0: Повільні фронти (щоб не було завад) */

    /* Безпека: При старті гарантовано вимикаємо реле (0В на затвор) */
    RELAY_PORT_ODR &= ~RELAY_PIN;

    /* ---------------------------------------------------------------------- */
    /* 3. ТЕРМІСТОР NTC (АЦП): PC4                                            */
    /* Налаштування: ВХІД, ПЛАВАЮЧИЙ (Floating).                              */
    /* Причина: Щоб внутрішня підтяжка не змінила напругу вимірювального кола.*/
    /* ---------------------------------------------------------------------- */
    NTC_PORT_DDR &= ~NTC_PIN;
    NTC_PORT_CR1 &= ~NTC_PIN;
    NTC_PORT_CR2 &= ~NTC_PIN;

    /* ---------------------------------------------------------------------- */
    /* 4. ДИСПЛЕЙ TM1637: PC6 (DIO), PC7 (CLK)                                */
    /* Налаштування: ВИХІД, OPEN-DRAIN, висока швидкість (10 МГц).            */
    /* Причина: Транзистори зсуву рівнів Q1, Q2 мають зовнішні підтяжки до 3.3V.*/
    /* ---------------------------------------------------------------------- */
    TM1637_PORT_DDR |= (TM1637_DIO_PIN | TM1637_CLK_PIN);  /* 1: Вихід */
    TM1637_PORT_CR1 &= ~(TM1637_DIO_PIN | TM1637_CLK_PIN); /* 0: Відкритий стік (Open-Drain)! */
    TM1637_PORT_CR2 |= (TM1637_DIO_PIN | TM1637_CLK_PIN);  /* 1: Швидкі фронти для цифрової шини */
    
    /* За замовчуванням відпускаємо шину (1 в регістрі ODR для Open-Drain 
       означає "відключити землю", зовнішній резистор підтягне до 3.3V) */
    TM1637_PORT_ODR |= (TM1637_DIO_PIN | TM1637_CLK_PIN);
}