/**
 * @file    adc.h
 * @brief   Драйвер 10-бітного АЦП (ADC1) для STM8S103F3P6.
 * @details Налаштований на опитування (Polling), Праве вирівнювання (10 біт)
 * та роботу з дільником частоти (f_ADC = 4 МГц).
 */

#ifndef ADC_H
#define ADC_H

#include "board_config.h"
/* ========================================================================== */
/* ЛОКАЛЬНІ МАСКИ РЕГІСТРІВ АЦП (Згідно з RM0016)                             */
/* ========================================================================== */
#define ADC1_CSR_CH_MASK            ((uint8_t)0x0F)
#define ADC1_CSR_EOC                ((uint8_t)(1 << 7))

#define ADC1_CR1_ADON               ((uint8_t)(1 << 0))
#define ADC1_CR1_SPSEL_MASK         ((uint8_t)((1 << 6) | (1 << 5) | (1 << 4)))
#define ADC1_CR1_SPSEL_FMASTER_DIV4 ((uint8_t)(1 << 5))

#define ADC1_CR2_ALIGN_RIGHT        ((uint8_t)(1 << 3))

/**
 * @brief   Ініціалізація апаратного блоку ADC1.
 * @details Налаштовує:
 * - Мультиплексор на канал AIN2 (пін PC4).
 * - Дільник частоти на 4 (з 16 МГц отримуємо дозволені 4 МГц).
 * - Праве вирівнювання даних (для точності 1024 кроки).
 * - Подає живлення на аналоговий блок (Wake-up).
 * @param   None
 * @return  None
 */
void ADC_Init(void);

/**
 * @brief   Одиночне зчитування напруги з термістора.
 * @details Генерує програмний тригер старту, чекає підняття прапорця EOC (~3.5 мкс).
 * Зчитує дані з апаратним блокуванням (спочатку DRL, потім DRH).
 * @param   None
 * @return  uint16_t Значення АЦП від 0 до 1023.
 */
uint16_t ADC_Read(void);

#endif /* ADC_H */